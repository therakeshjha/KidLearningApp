/** Get questions for a subject, grade, and specific difficulty */
export function getQuestions(subject, grade, difficulty) {
  return QUESTIONS.filter(
    q => q.subject === subject && q.grade === grade && q.difficulty === difficulty
  )
}

/** Get a shuffled set of N questions for a session */
// Fisher-Yates shuffle for true randomization
function shuffle(array) {
  const arr = [...array]
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]]
  }
  return arr
}

// Shuffle answer options while tracking correct answer
function shuffleQuestion(question) {
  const indices = [0, 1, 2, 3]
  const shuffledIndices = shuffle(indices)

  return {
    ...question,
    options: shuffledIndices.map(i => question.options[i]),
    correct: shuffledIndices.indexOf(question.correct)
  }
}

export function getSessionQuestions(subject, grade, difficulty, count = 10, answeredQuestionIds = []) {
  // Get questions for this subject, grade, and difficulty
  let pool = getQuestions(subject, grade, difficulty)

  // Filter out already answered questions
  pool = pool.filter(q => !answeredQuestionIds.includes(q.id))

  // If we don't have enough unanswered questions at this difficulty, add from other difficulties
  let extended = [...pool]
  if (extended.length < count) {
    const fallback = QUESTIONS.filter(
      q => q.subject === subject && q.grade === grade && q.difficulty !== difficulty && !answeredQuestionIds.includes(q.id)
    )
    extended = [...extended, ...fallback]
  }

  // If still not enough (user has answered all questions for this subject), reset the pool
  if (extended.length < count) {
    console.log(`All questions answered for ${subject}! Resetting question pool.`)
    pool = getQuestions(subject, grade, difficulty)
    extended = [...pool]
    if (extended.length < count) {
      const fallback = QUESTIONS.filter(
        q => q.subject === subject && q.grade === grade && q.difficulty !== difficulty
      )
      extended = [...extended, ...fallback]
    }
  }

  const shuffled = shuffle(extended)
  // Also shuffle answer options for each question
  return shuffled.slice(0, count).map(q => shuffleQuestion(q))
}

/** Get a quick batch for speed round (mix of all difficulties for given grade) */
export function getSpeedRoundQuestions(subject, grade, count = 30, answeredQuestionIds = []) {
  // Filter out already answered questions for this subject and grade
  let pool = QUESTIONS.filter(q => q.subject === subject && q.grade === grade && !answeredQuestionIds.includes(q.id))

  // If we don't have enough unanswered questions (user has answered all), reset the pool
  if (pool.length < count) {
    console.log(`All questions answered for ${subject} speed round! Resetting question pool.`)
    pool = QUESTIONS.filter(q => q.subject === subject && q.grade === grade)
  }

  // Shuffle questions and their answer options
  return shuffle(pool).slice(0, count).map(q => shuffleQuestion(q))
}

